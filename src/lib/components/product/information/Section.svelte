<p class="section__subtitle">About Us</p>
<p class="section__paragraph">The Phonograph Company, LLC was founded in 1977 by brothers Steve and Jeff Oliphant, to support their growing collecting of antique phonographs and gramophones which began six years earlier. With over 50 years of experience, the brothers are among the world’s foremost dealers in antique phonographs and gramophones. Steve is among the most relied upon authentication experts in the field, and the brothers have assisted beginning and advanced collectors, and institutions, in meeting their acquisition and disposition goals.</p>
<p class="section__paragraph">This website shows only a small sampling of our current inventory. We have, or have access to, almost any phonograph or gramophone. Contact us with your “wants”.</p>
